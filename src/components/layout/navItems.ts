export function normalizeNavSearchText(value: string) {
  return value
    .toLowerCase()
    .replace(/\bh[\s._-]*c[\s._-]*f\b/g, "hcf")
    .replace(/\bg[\s._-]*c[\s._-]*d\b/g, "gcd")
    .replace(/\bl[\s._-]*c[\s._-]*m\b/g, "lcm")
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

export function basicNavItemSearchText(item: NavItem, sectionTitle: string) {
  return normalizeNavSearchText([
    sectionTitle,
    item.title,
    item.route,
    ...(item.searchTerms ?? []),
  ].join(" "));
}
