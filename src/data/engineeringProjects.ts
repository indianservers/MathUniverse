export function projectsForDomain(domainId: string) {
  return engineeringProjects.filter((project) => project.domainId === domainId);
}

export function projectSummary() {
  const domainIds = new Set(engineeringMathDomains.map((domain) => domain.id));
  const coveredDomainIds = new Set(engineeringProjects.map((project) => project.domainId));
  return {
    projectCount: engineeringProjects.length,
    domainCount: domainIds.size,
    coveredDomainCount: Array.from(domainIds).filter((domainId) => coveredDomainIds.has(domainId)).length,
    capstoneCount: engineeringProjects.filter((project) => project.difficulty === "capstone").length,
    deliverableCount: engineeringProjects.reduce((sum, project) => sum + project.deliverables.length, 0),
    totalMinutes: engineeringProjects.reduce((sum, project) => sum + project.estimatedMinutes, 0),
  };
}
